<h4>Etape 1 : Bilan personnel</h4>

Le bilan personnel représente un travail d'introspection utile pour tout candidat souhaitant devenir franchisé . Avant de se lancer à son compte, il est important de faire un auto-examen et évaluer : sa motivation pour entreprendre, son profil et ses capacités de financement.
Votre motivation

Devenir franchisé est une aventure qui nécessite du temps, des ressources financières et de l'énergie. Par ailleurs, le projet comportant des risques, l'environnement familial doit également être favorable à une telle entreprise. Enfin, il est probable que vous soyez amener à travailler loin de chez vous. Une mobilité géographique est alors nécessaire. Alors, prêt pour ces sacrifices ?
Votre profil

Devenir franchisé c’est avant tout créer une entreprise . Un entrepreneur indépendant doit avoir des compétences en management, gestion, prise de décision, en relationnel et surtout des compétences commerciales ; qualités souvent demandées par les franchiseurs.
Votre capacité de financement

Le montant de l’apport personnel doit obligatoirement couvrir le 1/3 de l’investissement total. En fonction du secteur d’activité (alimentaire, prêt-à-porter...) et du franchiseur, les capitaux nécessaires peuvent aller de 5 000 à plus de 500 000 euros. Evaluez au mieux votre patrimoine et assurez-vous au préalable d’avoir des fonds propres suffisants. Le cas échéant, rapprochez de votre banque pour calculer votre capacité d'emprunt.
